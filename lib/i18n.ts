export function translateText(value: string, locale: Locale): string {
    if (locale === "zh-TW" || !value) return value;

    const whitespaceMatch = value.match(/^(\s*)([\s\S]*?)(\s*)$/);
    if (!whitespaceMatch) return value;

    const [, leadingWhitespace, text, trailingWhitespace] = whitespaceMatch;
    const directTranslation = englishTranslations[text];
    if (directTranslation) {
        return `${leadingWhitespace}${directTranslation}${trailingWhitespace}`;
    }

    const numberedHeading = text.match(/^(\d+\.\s*)(.+)$/);
    if (numberedHeading) {
        const translatedHeading = englishTranslations[numberedHeading[2]];
        if (translatedHeading) {
            return `${leadingWhitespace}${numberedHeading[1]}${translatedHeading}${trailingWhitespace}`;
        }
    }

    for (const [pattern, format] of dynamicTranslations) {
        const matches = text.match(pattern);
        if (matches) {
            return `${leadingWhitespace}${format(...matches)}${trailingWhitespace}`;
        }
    }

    return value;
}

export function isLocale(value: string | null | undefined): value is Locale {
    return value === "zh-TW" || value === "en";
}
